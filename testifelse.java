public class testifelse {
	public static void main(String[] args) {
		int x = 30;
		if(x==10) {//==等于还是不等于
			System.out.println("x的值是10");
		}else if (x == 20){
			System.out.println("x的值是20");
		}else if (x == 30){
			System.out.println("x的值是30");
		}else {
			System.out.println("x的值是这是else语句");
		}
			
	}
}